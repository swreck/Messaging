const CHAPTER_PURPOSE: Record<3 | 4 | 5, string> = {
  3: "ROI and measurable value",
  4: "others who already trust you",
  5: "what you want your audience to do next",
};

const EMPTY_CASE_FILL: Record<3 | 4 | 5, string> = {
  3: "any numerical metric",
  4: "an actual name or award",
  5: "a specific action they can take — a call link, an email, or a scheduling page",
};

// Round 4 Fix 9 — expanded from 5 to 10 entries so users don't see the
// same opening word for soft note 1 across multiple builds in a session.
export const VALUE_CLAIM_POOL: ReadonlyArray<string> = [
  "more compelling",
  "more persuasive",
  "sharper",
  "more direct",
  "stronger",
  "tighter",
  "clearer",
  "more memorable",
  "more concrete",
  "more credible",
];

// Internal counter for index-rotating selection. Module-level state.
let _valueClaimIdx = 0;
function pickValueClaim(): string {
  const claim = VALUE_CLAIM_POOL[_valueClaimIdx % VALUE_CLAIM_POOL.length];
  _valueClaimIdx += 1;
  return claim;
}

export function buildSoftNote(
  chapter: 3 | 4 | 5,
  missingPieces: string[]  // empty = whole chapter empty; non-empty = partial
): string {
  const purpose = CHAPTER_PURPOSE[chapter];
  const valueClaim = pickValueClaim();

  // Round 3.2 Item 4 — strip trailing punctuation from each piece before
  // joining. Some classifier outputs end with a period; the template
  // appends its own period after `${missingDescription}`, producing the
  // doubled-period bug Cowork observed (".. Anything I can use?").
  // Round 3.4 Bug 12 — dedupe pieces case-insensitively. Pat's verification
  // surfaced "with X and X. Anything I can use?" — the classifier returned
  // the same description twice in missingPieces. Drop exact-match repeats
  // before joining so the join template can't produce "and X and X".
  const cleaned = missingPieces.map(p => p.replace(/[.!?]+\s*$/, '').trim()).filter(p => p.length > 0);
  const seen = new Set<string>();
  const cleanedPieces: string[] = [];
  for (const p of cleaned) {
    const key = p.toLowerCase();
    if (seen.has(key)) continue;
    seen.add(key);
    cleanedPieces.push(p);
  }

  let missingDescription: string;
  if (cleanedPieces.length === 0) {
    missingDescription = EMPTY_CASE_FILL[chapter];
  } else if (cleanedPieces.length === 1) {
    missingDescription = cleanedPieces[0];
  } else if (cleanedPieces.length === 2) {
    missingDescription = `${cleanedPieces[0]} and ${cleanedPieces[1]}`;
  } else {
    const last = cleanedPieces[cleanedPieces.length - 1];
    const rest = cleanedPieces.slice(0, -1).join(", ");
    missingDescription = `${rest}, and ${last}`;
  }

  let question = `The chapter on ${purpose}, chapter ${chapter}, would be ${valueClaim} with ${missingDescription}. Anything I can use?`;

  // Round 3.4 Bug 12 — post-generation regex check. Belt-and-suspenders
  // against the duplicate-span case the classifier-dedup above doesn't
  // catch (e.g. near-duplicate phrases that differ only in casing or
  // whitespace). Pattern: any 20+-character span repeated consecutively
  // (with up to 3 connector words / whitespace between). When detected,
  // collapse the span to one occurrence rather than re-running the call —
  // re-rolling here is impractical because the enrichment question is
  // assembled deterministically from missingPieces, not generated by an
  // LLM at this layer.
  const dupSpanPattern = /(\b.{20,}?\b)\s+(?:and|,|\.|;|\s){0,15}\1/i;
  const m = dupSpanPattern.exec(question);
  if (m) {
    // Collapse the second occurrence to nothing, leaving a clean sentence.
    question = question.replace(dupSpanPattern, m[1]);
    console.log('[buildSoftNote] dropped duplicate clause (Bug 12 guard)');
  }

  return question;
}

