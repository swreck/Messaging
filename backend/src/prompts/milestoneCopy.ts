// ─── New feature — Stuck-state detection re-engage copy ───────────────
// Six trigger types, one locked re-engage line per trigger. Some templates
// take parameters (the current question, the next-step purpose) and the
// caller substitutes them — explanation-style wording per Cowork.

export const STUCK_IDLE =
  "Still here whenever you're ready. If you're not sure what to say, just tell me what you're stuck on and I'll help you through it.";

export const STUCK_EMPTY_SUBMIT =
  "Looks like that one came through blank. No rush — type whatever's on your mind, even partial, and I'll work with it.";

export function buildStuckExplicitHelp(restateQuestion: string): string {
  return `Of course. Let's break this down. The question I'm asking is ${restateQuestion}. Want me to give you a couple of examples of what people usually say here?`;
}

export function buildStuckMetaQuestion(
  currentStepPurpose: string,
  nextStep: string
): string {
  return `Fair question. Right now I'm trying to understand ${currentStepPurpose}. Once I have that, I'll ${nextStep}. Does that help, or do you want me to back up?`;
}

export function buildStuckOffTopic(restateQuestion: string): string {
  return `Got it. To make sure I'm following — were you answering ${restateQuestion}, or were we shifting to something else? Either way is fine, just want to make sure we're on the same page.`;
}

export function buildStuckOneWordReply(concreteExample: string): string {
  return `Got that. Can you give me a sentence or two more? Something like ${concreteExample}.`;
}

